function showInfo(region) {
    const hoverInfo = document.querySelector('.hover-info');
    const data = infoData[region];
    if (!data) return;
    hoverInfo.innerHTML = `
<img src="${data.image}">
<h1>${data.title}</h1>
<div class="outerLayer">
  <div>
    <h1>Capital</h1>
    <img src="${data.capitalImage}">
    <h2>${data.capital}</h2>
  </div>
  <div>
    <h1>Chief Minister</h1>
    <img src="${data.cmImage}">
    <h2>${data.cm}</h2>
  </div>
</div>
`;
    hoverInfo.style.display = 'block';
}

function hideInfo() {
    const hoverInfo = document.querySelector('.hover-info');
    hoverInfo.innerHTML = `<h1 style="font-size: 5vw;">Map of India</h1>
<div style="position: absolute; top:50%; left:50%; transform: translate(-50%,-50%);">
  <img class="indiaFlag" src="images/flag.gif" style="border-radius: 100%;" onclick="playSong()">
  <h1 style="margin-top: 4%; padding: 6%; border: 5px ridge rgb(92,46,46); background-image: linear-gradient(to bottom, black, #ff9933, white, green, black);">
    Hover over different states for more information about them!
  </h1>
</div>`;
}

let playing = false;
function playSong() {
    const aud = document.getElementById("audio");
    if (!playing) {
        aud.play();
        playing = true;
    } else {
        aud.pause();
        playing = false;
    }
}
